class  CustomMath {
  public static double square(double numIn){
    return (numIn*numIn);
  }

  public static int square(int numIn){
    return (numIn*numIn);
  }

  public static int multiply(int numIn1,int numIn2){
    return(numIn1*numIn2);

  }
  public static double multiply(double numIn1,double numIn2){
    return(numIn1*numIn2);

  }

  public static double divide(double numIn1,double numIn2){
    return(numIn1/numIn2);

  }
}