void Color(string color = "NA")
{
	if(color == "NA")
		cout << "\033[0m";
	else if(color == "black")
		cout << "\033[30m";
	else if(color == "red")
		cout << "\033[31m";
	else if(color == "green")
		cout << "\033[32m";
	else if(color == "gold")
		cout << "\033[33m";
	else if(color == "blue")
		cout << "\033[34m";
	else if(color == "purple")
		cout << "\033[35m";
	else if(color == "cyan")
		cout << "\033[36m";
}